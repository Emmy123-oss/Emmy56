# Constant (usually written in ALL CAPS by convention)
YEAR = 2025

# Variables (values may change)
birth_rate = 18.5   # births per 1000 people
death_rate = 7.3    # deaths per 1000 people

# Calculate natural population growth (birth rate - death rate)
population_growth_rate = birth_rate - death_rate

# Print results
print("Population Statistics for the Year:", YEAR)
print("Birth Rate:", birth_rate, "per 1000 people")
print("Death Rate:", death_rate, "per 1000 people")
print("Natural Population Growth Rate:", population_growth_rate, "per 1000 people")

